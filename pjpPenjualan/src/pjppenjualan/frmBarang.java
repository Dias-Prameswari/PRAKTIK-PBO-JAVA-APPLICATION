    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        String tkode = txt_kd_barang.getText();
        String tnama = txt_nm_barang.getText();
//        sSatuan = combo_satuan.getSelectedItem().toString();
//        String tSatuan = (String) combo_satuan.getSelectedItem();
        double hrg = Double.parseDouble(txt_harga_jual.getText());
        double hrg_beli = Double.parseDouble(txt_harga_beli.getText());
        int stk = Integer.parseInt(txt_stok.getText());
        int stk_min = Integer.parseInt(txt_stok_min.getText());
        
        try{
        if (edit==true){
        stm.executeUpdate("update barang set nm_barang='"+tnama+"',satuan='"+sSatuan+"',harga_jual="+hrg+",harga_beli="+hrg_beli+",stok="+stk+",stok_min="+stk_min+" where kd_barang='" + tkode + "'");
        }else
        {
        stm.executeUpdate("INSERT into barang VALUES('"+tkode+"','"+tnama+"','"+sSatuan+"',"+hrg+","+hrg_beli+","+stk+","+stk_min+")");
        }
        hasil_table.setModel(new DefaultTableModel(dataTable,header));
        baca_data();
        aktif(false);
        setTombol(true);
        }catch(SQLException e) {
        JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btn_simpanActionPerformed

